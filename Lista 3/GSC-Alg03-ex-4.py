numLados = int(input('Insira o número de lados do polígono (3 a 10): '))

if (numLados == 3) :

    print('É um triângulo')

elif (numLados == 4) :

    print('É um quadrado')

elif (numLados == 5) :

    print('É um pentágono')

elif (numLados == 6) :

    print('É um hexágono')

elif (numLados == 7) :

    print('É um heptágono')

elif (numLados == 8) :

    print('É um octógono')

elif (numLados == 9) :

    print('É um eneágono')

elif (numLados == 10) :

    print('É um decágono')

else :

    print('Erro. Número inválido')